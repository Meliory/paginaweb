// Obtener traducci√≥n por clave
function getTranslation(key, language = null) {
    const lang = language || getCurrentLanguage();
    const keys = key.split('.');
    let translation = TRANSLATIONS[lang];
    
    for (const k of keys) {
        if (translation && translation[k]) {
            translation = translation[k];
        } else {
            // Fallback al espa√±ol si no existe la traducci√≥n
            translation = TRANSLATIONS['es'];
            for (const fallbackKey of keys) {
                if (translation && translation[fallbackKey]) {
                    translation = translation[fallbackKey];
                } else {
                    return key; // Devolver la clave si no hay traducci√≥n
                }
            }
        }
    }
    
    return translation || key;
}

// Obtener idioma actual
function getCurrentLanguage() {
    return localStorage.getItem('language') || 'es';
}

// Traducir toda la p√°gina
function translatePage(language = null) {
    const lang = language || getCurrentLanguage();
    
    // Traducir elementos con data-translate
    const elementsToTranslate = document.querySelectorAll('[data-translate]');
    
    elementsToTranslate.forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = getTranslation(key, lang);
        
        if (element.tagName === 'INPUT' && element.type === 'text') {
            element.placeholder = translation;
        } else {
            element.textContent = translation;
        }
    });
    
    // Traducir elementos con data-translate-html (para contenido HTML)
    const htmlElements = document.querySelectorAll('[data-translate-html]');
    htmlElements.forEach(element => {
        const key = element.getAttribute('data-translate-html');
        const translation = getTranslation(key, lang);
        element.innerHTML = translation;
    });
    
    // Actualizar el idioma en localStorage
    localStorage.setItem('language', lang);
    
    // Disparar evento personalizado para que otros scripts puedan reaccionar
    document.dispatchEvent(new CustomEvent('languageChanged', { 
        detail: { language: lang } 
    }));
}

// Configurar eventos de cambio de idioma
function setupLanguageListeners() {
    // Selectors de idioma en el navegador
    const languageOptions = document.querySelectorAll('.language-option');
    const mobileLanguageOptions = document.querySelectorAll('.mobile-language-option');
    
    // Funci√≥n para manejar cambio de idioma
    function handleLanguageChange(event) {
        event.preventDefault();
        const selectedLang = event.target.closest('[data-lang]').getAttribute('data-lang');
        
        // Actualizar idioma activo
        updateActiveLanguage(selectedLang);
        
        // Traducir p√°gina
        translatePage(selectedLang);
        
        // Cerrar dropdown (si existe)
        const dropdown = document.querySelector('.language-dropdown');
        if (dropdown) {
            dropdown.classList.remove('active');
        }
        
        // Cerrar men√∫ m√≥vil (si existe)
        const mobileMenu = document.querySelector('.mobile-menu');
        const hamburger = document.querySelector('.hamburger');
        const overlay = document.querySelector('.mobile-menu-overlay');
        
        if (mobileMenu && mobileMenu.classList.contains('active')) {
            mobileMenu.classList.remove('active');
            hamburger.classList.remove('active');
            overlay.classList.remove('active');
            document.body.classList.remove('menu-open');
        }
    }
    
    // A√±adir listeners
    languageOptions.forEach(option => {
        option.addEventListener('click', handleLanguageChange);
    });
    
    mobileLanguageOptions.forEach(option => {
        option.addEventListener('click', handleLanguageChange);
    });
    
    // Actualizar bot√≥n de idioma y estado activo al cargar
    updateActiveLanguage(getCurrentLanguage());
}

// Actualizar idioma activo en la interfaz
function updateActiveLanguage(lang) {
    // Actualizar bot√≥n principal
    const languageButton = document.querySelector('.language-button span');
    if (languageButton) {
        languageButton.textContent = lang.toUpperCase();
    }
    
    // Actualizar opciones activas (desktop)
    const allOptions = document.querySelectorAll('.language-option');
    allOptions.forEach(option => {
        option.classList.remove('current');
        if (option.getAttribute('data-lang') === lang) {
            option.classList.add('current');
        }
    });
    
    // Actualizar opciones activas (m√≥vil)
    const allMobileOptions = document.querySelectorAll('.mobile-language-option');
    allMobileOptions.forEach(option => {
        option.classList.remove('current');
        if (option.getAttribute('data-lang') === lang) {
            option.classList.add('current');
        }
    });
}

// Inicializar traducciones cuando el DOM est√© listo
document.addEventListener('DOMContentLoaded', function() {
    translatePage();
    setupLanguageListeners();
});

